"""
Утилиты для обработки и нормализации номеров телефонов
"""
import re

def normalize_phone(phone: str) -> str:
    """
    Приводит номер телефона к единому формату +79999999999
    
    1. Если в строке несколько телефонов (через запятую/слэш), берем первый
    2. Удаляет все нецифровые символы
    3. Если номер начинается с 8, заменяет на 7
    4. Если номер начинается с 7 без плюса, добавляет плюс
    5. Если номер без 7/8 в начале (10 цифр), добавляет +7
    6. Итоговый формат всегда +7... (11 цифр после плюса)
    7. Ограничение длины 20 символов
    """
    if not phone:
        return ""
    
    # 0. Обработка списка телефонов
    # Если передано несколько номеров (через , ; /), берем первый
    phone_str = str(phone).split(',')[0].split(';')[0].split('/')[0].strip()
    
    # 1. Оставляем только цифры
    digits = re.sub(r'\D', '', phone_str)
    
    if not digits:
        return ""

    # 2. Обработка префикса (РФ стандарт)
    if len(digits) == 10:
        digits = "7" + digits
    elif len(digits) == 11:
        if digits.startswith("8"):
            digits = "7" + digits[1:]
    
    # 3. Возвращаем в формате +7... (11 цифр) или +... (если формат нестандартный)
    # Ограничиваем общую длину 20 символами (включая плюс)
    result = f"+{digits}"
    return result[:20]

def format_phone_iiko(phone: str) -> str:
    """
    Формат для iiko (только цифры, без +)
    """
    norm = normalize_phone(phone)
    return re.sub(r'\D', '', norm)
