"""
Утилиты для обработки и нормализации номеров телефонов
"""
import re

def normalize_phone(phone: str) -> str:
    """
    Приводит номер телефона к единому формату +79999999999
    
    1. Удаляет все нецифровые символы
    2. Если номер начинается с 8, заменяет на 7
    3. Если номер начинается с 7 без плюса, добавляет плюс
    4. Если номер без 7/8 в начале (10 цифр), добавляет +7
    5. Итоговый формат всегда +7... (11 цифр после плюса)
    """
    if not phone:
        return ""
    
    # 1. Оставляем только цифры
    # str(phone) гарантирует работу с любым типом, re.sub убирает все кроме цифр
    digits = re.sub(r'\D', '', str(phone))
    
    if not digits:
        return ""

    # 2. Обработка префикса (РФ стандарт)
    # Если 10 цифр (912...), превращаем в 11 (7912...)
    if len(digits) == 10:
        digits = "7" + digits
    # Если 11 цифр и начинается с 8, меняем на 7
    elif len(digits) == 11:
        if digits.startswith("8"):
            digits = "7" + digits[1:]
    
    # 3. Возвращаем в формате +7... (11 цифр) или +... (если формат нестандартный)
    # Мы всегда возвращаем с ОДНИМ плюсом в начале
    return f"+{digits}"

def format_phone_iiko(phone: str) -> str:
    """
    Формат для iiko (только цифры, без +)
    """
    norm = normalize_phone(phone)
    return re.sub(r'\D', '', norm)
